/**
 * Get policy for a learner based on their grade
 */
export function getPolicyForGrade(grade: number): GradeBandPolicy {
  if (grade >= 1 && grade <= 3) return GRADE_BAND_POLICIES.grades_1_3;
  if (grade >= 4 && grade <= 6) return GRADE_BAND_POLICIES.grades_4_6;
  if (grade >= 7 && grade <= 9) return GRADE_BAND_POLICIES.grades_7_9;
  if (grade >= 10 && grade <= 12) return GRADE_BAND_POLICIES.grades_10_12;
  
  // Default to middle school
  return GRADE_BAND_POLICIES.grades_7_9;
}

/**
 * Get age band from grade
 */
export function getAgeBandFromGrade(grade: number): AgeBand {
  if (grade >= 1 && grade <= 3) return 'grades_1_3';
  if (grade >= 4 && grade <= 6) return 'grades_4_6';
  if (grade >= 7 && grade <= 9) return 'grades_7_9';
  return 'grades_10_12';
}

/**
 * Check if feature is available for grade
 */
export function isFeatureAvailable(
  grade: number,
  feature: keyof GradeBandPolicy
): boolean {
  const policy = getPolicyForGrade(grade);
  return !!policy[feature];
}

/**
 * Get AI Coach modes for grade
 */
export function getAICoachModesForGrade(grade: number): Array<'hint' | 'rubric_check' | 'debug' | 'critique'> {
  const policy = getPolicyForGrade(grade);
  const modes: Array<'hint' | 'rubric_check' | 'debug' | 'critique'> = [];
  
  if (policy.aiCoach.modes.hint) modes.push('hint');
  if (policy.aiCoach.modes.rubricCheck) modes.push('rubric_check');
  if (policy.aiCoach.modes.debug) modes.push('debug');
  if (policy.aiCoach.modes.critique) modes.push('critique');
  
  return modes;
}
