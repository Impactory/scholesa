/**
 * Grade Band Policy System
 * 
 * Age-appropriate feature gating based on developmental appropriateness
 * K-3, 4-6, 7-9, 10-12
 */

import type { AgeBand } from '@/src/types/schema';

export interface GradeBandPolicy {
  // Core settings
  ageBand: AgeBand;
  minGrade: number;
  maxGrade: number;
  
  // Sprint & Mission settings
  maxSprintMinutes: number;
  minSprintMinutes: number;
  missionChoiceCount: 2 | 3;
  allowCustomMissions: boolean;
  
  // AI Coach modes
  aiCoach: {
    enabled: boolean;
    modes: {
      hint: boolean;
      rubricCheck: boolean;
      debug: boolean;
      critique: boolean;
    };
    requireTeacherGuidance: boolean;
    explainBackRequired: boolean;
  };
  
  // Social features
  social: {
    peerFeedback: {
      enabled: boolean;
      requireTemplate: boolean; // Must use "I like / I wonder / Next step"
      allowFreeform: boolean;
    };
    peerMessaging: {
      enabled: boolean;
      moderationRequired: boolean;
    };
    publicShowcase: {
      enabled: boolean;
      defaultVisibility: 'crew' | 'site' | 'private';
    };
    recognitionTokens: {
      enabled: boolean;
      types: string[]; // Which recognition types are shown
    };
  };
  
  // Reflection settings
  reflection: {
    promptCount: 1 | 2 | 3 | 4;
    emojiScalesEnabled: boolean;
    strategyPromptEnabled: boolean;
    evidenceLinkingEnabled: boolean; // Can link to specific artifacts
  };
  
  // Rubric complexity
  rubric: {
    levelCount: 2 | 3 | 4; // "I can..." vs 3-level vs detailed
    showExemplars: boolean;
    allowResubmission: boolean;
    studentFacingLanguage: 'simple' | 'standard' | 'detailed';
  };
  
  // Portfolio settings
  portfolio: {
    studentOwned: boolean;
    visibility: {
      student: boolean;
      parent: boolean;
      public: boolean;
      exportable: boolean;
    };
    autoGenerated: boolean;
    allowCustomization: boolean;
  };
  
  // Gamification
  gamification: {
    badges: 'stickers' | 'standard' | 'professional';
    showStreaks: boolean;
    showProgressBars: boolean;
    publicLeaderboards: boolean; // Always false per requirements
  };
  
  // UI/UX preferences
  ui: {
    iconOverText: boolean; // Big buttons with icons vs text-heavy
    audioFeedback: boolean; // Teacher audio notes
    simplifiedNavigation: boolean;
  };
  
  // Safety & moderation
  safety: {
    contentModeration: 'strict' | 'standard' | 'relaxed';
    requireParentConsent: boolean;
    mentorAccess: boolean;
    publicProfile: boolean;
  };
}

